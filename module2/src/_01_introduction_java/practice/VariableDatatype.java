package _01_introduction_java.practice;

public class VariableDatatype {
    public static void main(String[] args) {
        int i = 10;// khai bao bien i la kieu int có gia tri la 10
        float f = 20.5f;// khai bao bien f co kieu la float có gia tri la 20.5
        double d = 20.5;// khai bao bien d co kieu du lieu la double co gia tri la 20.5
        boolean b = true;// khai bao bien b co kieu du lieu la char co gia tri la true
        char c = 'a';// khai bao bien c co kieu la char co gia tri la ki tu a
        String s = "Hà Nội"; // khai báo biến s có kiểu là String có giá trị chuổi là hà nội
        System.out.println("i="+i);
        System.out.println("f="+f);
        System.out.println("d="+d);
        System.out.println("b="+b);
        System.out.println("c="+c);
        System.out.println("s="+s);
    }
}
